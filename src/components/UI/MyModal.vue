<template>
  <div class="modal" @click="hideModal" @keyDown="hideModal">
    <slot></slot>
  </div>
</template>

<script>
export default {
  name: 'my-modal',
  methods: {
    hideModal() {
      this.$emit('hide');
    },
  },
};
</script>

